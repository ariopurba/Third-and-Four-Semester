package PBO1.ArrayObject;

/**
 *
 * @author old
 */
public class Mobil {
    private String merk;
    private String warna;

    public Mobil() {
    }

    public Mobil(String merk, String warna) {
        this.merk = merk;
        this.warna = warna;
    }

    public String getMerk() {
        return merk;
    }

    public void setMerk(String merk) {
        this.merk = merk;
    }

    public String getWarna() {
        return warna;
    }

    public void setWarna(String warna) {
        this.warna = warna;
    }
    
    
}
