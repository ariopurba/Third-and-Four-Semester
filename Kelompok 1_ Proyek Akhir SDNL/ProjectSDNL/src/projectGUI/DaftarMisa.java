    private void jButtonDaftarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonDaftarMouseClicked
        // TODO add your handling code here:
        if (jTextFieldNama.getText().equals("") || jTextFieldAlamat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Harap lengkapi data!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        } else {
            daftar();
            jTextAreaInfoMisa.setText("Tanggal : " + jadwalTerupdate.get(0).getTanggal() + "\nKuota : " + jadwalTerupdate.get(0).getKuota() + "\nImam : " + jadwalTerupdate.get(0).getImam());
            jTextFieldNama.setText("");
            jTextFieldAlamat.setText("");
            jComboBoxJadwalMisa.setSelectedIndex(0);
        }


    }//GEN-LAST:event_jButtonDaftarMouseClicked

