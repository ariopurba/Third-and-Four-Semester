    public void daftar() {
        String nama, alamat, id;
        int pilihMisa;
        nama = jTextFieldNama.getText();
        alamat = jTextFieldAlamat.getText();
        pilihMisa = jComboBoxJadwalMisa.getSelectedIndex();
        createId++;
        id = "G-" + createId;
        int jumlahNode = jadwalTerupdate.get(pilihMisa).getUmat().countNodes(jadwalTerupdate.get(pilihMisa).getUmat().getRoot());
        if (jumlahNode < jadwalTerupdate.get(pilihMisa).getKuota()) {
            jadwalTerupdate.get(pilihMisa).getUmat().insert(new Umat(nama, alamat, id));
            jadwalTerupdate.get(pilihMisa).setKuota(jadwalTerupdate.get(pilihMisa).getKuota() - 1);
            JOptionPane.showMessageDialog(null, "Kode pendaftaran kamu adalah " + id + ""
                    + "!\nSilahkan simpan untuk ditunjukan pada petugas, terima kasih.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Kuota penuh !", "Gagal mendaftar", JOptionPane.ERROR_MESSAGE);
        }
    }

