package model;

/**
 *
 * @author old
 */
public class Staff {
    private int idStaff;
    private String nama, alamat, no_hp, jabatan;
    
    public Staff(){
        
    }

    public Staff(int idStaff, String nama, String alamat, String no_hp, String jabatan) {
        this.idStaff = idStaff;
        this.nama = nama;
        this.alamat = alamat;
        this.no_hp = no_hp;
        this.jabatan = jabatan;
    }

    public int getIdStaff() {
        return idStaff;
    }

    public void setIdStaff(int idStaff) {
        this.idStaff = idStaff;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getNo_hp() {
        return no_hp;
    }

    public void setNo_hp(String no_hp) {
        this.no_hp = no_hp;
    }

    public String getJabatan() {
        return jabatan;
    }

    public void setJabatan(String jabatan) {
        this.jabatan = jabatan;
    }
    
}
